// Helper function to calculate distance based on coordinates
export function calculateDistanceFromUser(userLat: number, userLng: number, spotLat: number, spotLng: number): number {
  const R = 6371e3; // Earth's radius in meters
  const φ1 = userLat * Math.PI/180;
  const φ2 = spotLat * Math.PI/180;
  const Δφ = (spotLat-userLat) * Math.PI/180;
  const Δλ = (spotLng-userLng) * Math.PI/180;

  const a = Math.sin(Δφ/2) * Math.sin(Δφ/2) +
          Math.cos(φ1) * Math.cos(φ2) *
          Math.sin(Δλ/2) * Math.sin(Δλ/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));

  return Math.round(R * c); // Distance in meters
}

// Function to get spots with real-time distance calculations
export function getValleyGirlSpotsWithDistance(userLat: number, userLng: number) {
  return embeddedValleyGirlSpots.map(spot => {
    const calculatedDistance = calculateDistanceFromUser(userLat, userLng, spot.latitude, spot.longitude);
    return {
      ...spot,
      distance: calculatedDistance,
      address: `${calculatedDistance}m away`
    };
  }).sort((a, b) => a.distance - b.distance);
}